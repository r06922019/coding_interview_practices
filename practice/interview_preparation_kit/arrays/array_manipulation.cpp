// Complete the arrayManipulation function below.
long arrayManipulation(int n, vector<vector<int>> queries) {
    priority_queue<pair<int, int>> pq;
    for(auto &q : queries) {
        pq.push({ -q[0], q[2] });
        pq.push({ -(q[1]+1), -q[2] });
    }

    long ans = 0, cur = 0;
    int index = 1;
    while(index <= n && !pq.empty()) {
        while(!pq.empty() && pq.top().first >= -index) {
            cur += pq.top().second;
            pq.pop();
        }
        ans = max(ans, cur);
        index = -pq.top().first;
    }
    return ans;
}

